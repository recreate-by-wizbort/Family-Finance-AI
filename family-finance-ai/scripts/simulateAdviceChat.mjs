function looksFinancial(input) {
  const n = input.toLowerCase().replace(/ё/g, 'е').replace(/сегодня/g, ' ')
  if (/(умножить|разделить на|плюс \d|корень из|уравнен|пятнадцать умножить|километр(ов)? до|метров до|градусов)/i.test(n)
    && !/(сум|руб|тенге|бюджет|трат|накоп|вклад|карт|зарплат|кредит|ипотек|млн|тыс)/i.test(n)) {
    return false
  }
  return FINANCE_KEYWORDS.some((kw) => n.includes(kw))
}

const SAMPLE_PERIOD = {
  start: new Date(2025, 2, 1),
  end: new Date(2025, 2, 31, 23, 59, 59, 999),
}

function routeQuestion(q, opts = {}) {
  const {
    source = 'input',
    periodRange = null,
    askedClarification = false,
  } = opts
  const isActionTrigger = source === 'action'
  const sq = String(q || '').trim()
  if (!sq) return { type: 'empty' }

  const spec = detectSpecialInput(sq)
  if (spec) return { type: `special:${spec.type}`, preview: spec.r.slice(0, 140) }

  if (sq.length < 5) return { type: 'too_short', preview: TOO_SHORT_MSG }

  if (isActionTrigger && /план.*машин/i.test(sq) && periodRange) {
    return { type: 'car_plan', preview: '(динамический план на машину)' }
  }

  if (/^посоветовать\.?$/i.test(sq) && periodRange) {
    return { type: 'inv_advise', preview: '(generateDynamicResponse inv_advise — скрипт в analysisEngine)' }
  }

  const faq = matchFAQ(sq)
  if (faq?.r) {
    const t = faq.type ? `special:${faq.type}` : 'faq'
    return { type: t, preview: faq.r.slice(0, 160) }
  }

  if (!looksFinancial(sq) && !askedClarification) {
    return { type: 'clarification_list', preview: '(список категорий + уточнение)' }
  }

  return { type: 'would_call_ai', preview: '(askAI — нужен ключ)' }
}

